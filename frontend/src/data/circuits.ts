/**
 * Trazados de circuitos para el Track Map.
 *
 * Fuentes:
 *   1. F1 circuits (Spa, Catalunya): coordenadas GPS reales del proyecto público
 *      `bacinger/f1-circuits` (MIT license).
 *   2. Otros (Jarama, Cheste, Castellolí, Navarra, Jerez): coordenadas GPS reales
 *      de OpenStreetMap obtenidas vía Overpass API filtrando `highway=raceway`.
 *   3. MotorLand Aragón: trazado a mano con varias curvas y la recta de 1.2km
 *      característica (no mapeado en OSM con esa etiqueta).
 *
 * Las coordenadas (lng, lat) pasan por `normalizeGeoJson` que las proyecta al
 * espacio 0-100 preservando aspect ratio con corrección por latitud media.
 */

export interface Circuit {
  name: string;
  aliases: string[];
  /** Array de [x, y] en rango ~0-100. */
  path: [number, number][];
  /** Porcentajes del path donde acaban S1 y S2 (S3 va hasta el final). */
  sectorBoundaries: [number, number];
  country?: string;
  length_km?: number;
  /** true si las coordenadas provienen de GeoJSON / OSM real (precisas). */
  realData?: boolean;
}

// ─── Helper: normaliza GeoJSON (lng, lat) → coordenadas locales 0-100 ────────
function normalizeGeoJson(coords: [number, number][]): [number, number][] {
  const lngs = coords.map((c) => c[0]);
  const lats = coords.map((c) => c[1]);
  const minLng = Math.min(...lngs);
  const maxLng = Math.max(...lngs);
  const minLat = Math.min(...lats);
  const maxLat = Math.max(...lats);
  const rangeLng = maxLng - minLng;
  const rangeLat = maxLat - minLat;
  // Corrección de aspect ratio: a 50ºN un grado de lng vale menos que uno de lat.
  // Usar coseno de la latitud media para que el círculo no quede achatado.
  const meanLatRad = ((minLat + maxLat) / 2) * (Math.PI / 180);
  const lngScale = Math.cos(meanLatRad);
  const effLngRange = rangeLng * lngScale;
  const maxRange = Math.max(effLngRange, rangeLat);
  const padding = 5;
  // Centrar horizontalmente si el ancho efectivo es menor que el alto y viceversa
  const xPadding = (maxRange - effLngRange) / 2;
  const yPadding = (maxRange - rangeLat) / 2;
  return coords.map(([lng, lat]) => {
    const xCentered = (lng - minLng) * lngScale + xPadding;
    const yCentered = lat - minLat + yPadding;
    const x = padding + (xCentered / maxRange) * (100 - 2 * padding);
    // Invertir Y porque en pantalla crece hacia abajo
    const y = padding + ((maxRange - yCentered) / maxRange) * (100 - 2 * padding);
    return [x, y];
  });
}

