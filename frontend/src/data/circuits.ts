export function findCircuit(name?: string | null): Circuit | null {
  if (!name) return null;
  const normalized = name
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/circuit(o|s)?\s+(de|del|of)?\s*/i, '')
    .replace(/\s+/g, ' ')
    .trim();

  for (const c of CIRCUITS) {
    const candidates = [c.name, ...c.aliases].map((s) =>
      s.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '').trim()
    );
    if (candidates.some((alias) => normalized === alias || normalized.includes(alias) || alias.includes(normalized))) {
      return c;
    }
  }
  return null;
}
